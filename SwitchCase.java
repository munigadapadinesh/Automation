package java_prep;

public class SwitchCase {

	public static void main(String[] args) {

		int b = 1;
		
		switch(b) {
		
		case 0:
			System.out.println("script");
			break;
			
		case 1:
			System.out.println("case 1 executed");
			
			
		case 3:
			System.out.println("Case 3 executed");
			
			
		default :
			System.out.println("default executed");
		
	}
			
			}

	}


